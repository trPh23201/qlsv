package DTO;

/**
 *
 * @author gnoob
 */
public class Course {
    private int id;
    private String name;
    private String period;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPeriod() {
        return period;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPeriod(String period) {
        this.period = period;
    }

 
    
}
